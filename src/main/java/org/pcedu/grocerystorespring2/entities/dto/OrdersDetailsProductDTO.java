package org.pcedu.grocerystorespring2.entities.dto;

import java.math.BigDecimal;

/**
 *
 * @author George.Pasparakis
 */
public class OrdersDetailsProductDTO extends OrdersDetailsDTO {
    private Integer productId;
    private String productName;
    private BigDecimal productPrice;
    private int productQuantity;

    public OrdersDetailsProductDTO() {
    }

    public OrdersDetailsProductDTO(Integer productId, String productName, BigDecimal productPrice, int productQuantity) {
        this.productId = productId;
        this.productName = productName;
        this.productPrice = productPrice;
        this.productQuantity = productQuantity;
    }

    public Integer getProductId() {
        return productId;
    }

    public void setProductId(Integer productId) {
        this.productId = productId;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public BigDecimal getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(BigDecimal productPrice) {
        this.productPrice = productPrice;
    }

    public int getProductQuantity() {
        return productQuantity;
    }

    public void setProductQuantity(int productQuantity) {
        this.productQuantity = productQuantity;
    }
}
